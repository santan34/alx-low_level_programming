/**
 * _pow_recursion - recreate the power function
 * @x: number to be raised to a certain power
 * @y: number to rise to
 * Return: x to the power y
 */
int _pow_recursion(int x, int y)
{
	if (y < 0)
	{
		return (-1);
	}
	else if (y == 0)
	{
		return (1);
	}
	else if (y == 1)
	{
		return (x);
	}
	else
	{
		return (x * _pow_recursion(x, y - 1));
	}
}
